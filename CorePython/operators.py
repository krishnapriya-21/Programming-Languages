
#Assigning integer values to variables
a=10
b=4

print (f"The two numbers are {a}, {b}")


#Arithmetic operators

#add operator to sum two numbers
print("addition of two numbers is :" , a+b)

#subtract operator
print("Subtraction of two numbers is : " , a-b)

#product operator
print("Product of two numbers is :",a*b)

#modulus operator
print("Modulus of two numbers is : ",a%b)

#division operator
print("Division of two numbers is : ",a/b)

#power operator
print("Power of 10 and 4 is : ",a**b)

#Comparison operators

print("a equals b", a==b)

print("a greater than b", a>b)

print("a lesser than b", a<b)

print("a is greater than or equal to b", a>=b)

print("a is lesser than or equal to b", a<=b)

# logical operators


#Logical AND operator
print("A is greater than 0 and B is greater than 0" , a>0 and b>0)

#Logical OR operator
print("A is greater than 10 and B is greater than 0", a>10 or b>0)

#Logical NOT operator
print("Reverse of A is greater than B", not(a>b))

